// Utility functions
export const getInstrumentBySymbol = (symbol: string): TradingInstrument | undefined => {
  if (!symbol) return undefined;
  return TRADING_DATABASE.find(instrument => 
    instrument.symbol.toLowerCase() === symbol.toLowerCase()
  );
};

export const getInstrumentsByType = (type: TradingInstrument['type']): TradingInstrument[] => {
  return TRADING_DATABASE.filter(instrument => instrument.type === type);
};

export const getInstrumentsBySector = (sector: string): TradingInstrument[] => {
  return TRADING_DATABASE.filter(instrument => 
    instrument.sector?.toLowerCase().includes(sector.toLowerCase())
  );
};

export const searchInstruments = (query: string): TradingInstrument[] => {
  const lowerQuery = query.toLowerCase();
  return TRADING_DATABASE.filter(instrument =>
    instrument.symbol.toLowerCase().includes(lowerQuery) ||
    instrument.name.toLowerCase().includes(lowerQuery) ||
    instrument.sector?.toLowerCase().includes(lowerQuery)
  );
};

// Alternative function name for compatibility
export const searchTradingInstruments = searchInstruments;

